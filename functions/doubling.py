# - Create a variable named `base_number` and assign the value `123` to it
# - Create a function called `double_number()` that doubles it's input parameter
#   and returns the doubled value
# - Print the result of `double_number(base_number)`

base_number = '123'


def double_number(number):
    return int(number) * 2


print(double_number(base_number))
